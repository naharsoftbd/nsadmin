    public function index()
    {
        if(Auth::user()->hasRole('admin')){

            $users = User::all();
            return Inertia::render('Users/Users', [
                'users' => $users,
                'status' => session('status'),
            ]);

        }else{

            return redirect()->intended('/dashboard');
        }
    }

